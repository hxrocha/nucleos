    private void btnIniciarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarPedidoActionPerformed
       //Verifica se o caixa esta aberto
        RetornoDoDAO  respostaSeTemCaixaAberto;
        respostaSeTemCaixaAberto = caixaDAO.temCaixaAberto();
        if(!respostaSeTemCaixaAberto.executadoComSucesso){
            JOptionPane.showMessageDialog(null,respostaSeTemCaixaAberto.mensagem,"Erro",JOptionPane.ERROR_MESSAGE); 
        }
        else{
        
        //tenho que verificar se foi executado com sucesso
         if(!respostaSeTemCaixaAberto.valorDoRetornoBooleano){
            JOptionPane.showMessageDialog(null,respostaSeTemCaixaAberto.mensagem,"Erro",JOptionPane.ERROR_MESSAGE); 
        }else{
        RetornoDoDAO respostaNumeroDoCaixa = caixaDAO.numeroDoCaixaAberto();
        if(respostaNumeroDoCaixa.executadoComSucesso){
            int numeroDoCaixaAberto  = respostaNumeroDoCaixa.valorRetornadoInteiro;
            // Se tiver pedido aberto vai para ele. Caso contr√°rio abre um
            PedidoDAO dao = new PedidoDAO("scv","root");
            int num = 0;
            int aberto = 0;
            try {
                aberto = dao.pedidosEmAberto();
            } catch (SQLException ex) {
            Logger.getLogger(FrenteDeCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
        if(aberto == 0){
            num = dao.inicia();
        }else{
            try {
                num = dao.pegaPedidoAberto().getCodigoDoPedido();
            } catch (SQLException ex) {
                Logger.getLogger(FrenteDeCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        FrmVendas frm = new FrmVendas(num,numeroDoCaixaAberto);
        frm.setVisible(true);
        }        
         }    }
    }//GEN-LAST:event_btnIniciarPedidoActionPerformed

