    private void lerDadosIniciais() {
        try {
            produtos = dao.buscarTodosProdutos();
            MyTableModel tab = new MyTableModel(Produto.class,produtos,tabelaDeProdutos);
            tabelaDeProdutos.setModel(tab);
        } catch (SQLException ex) {
            Logger.getLogger(FrmProduto.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro: "+ex.getMessage());
        }
    }
