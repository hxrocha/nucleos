    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
 if(this.tabelaDeProdutos.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(null,"Você deve selecionar um Produto");
        else{
           int resp = JOptionPane.showConfirmDialog(this,"Você tem Certeza",
                   "Apagar o Produto Selecionado",
                   JOptionPane.OK_CANCEL_OPTION,
                   JOptionPane.QUESTION_MESSAGE);
           if(resp == 0){
           int cod = produtos.get(this.tabelaDeProdutos.getSelectedRow()).getCodigoDoProduto();
           dao.apagar(cod);
           lerDadosIniciais();
              
           }
        }
    }//GEN-LAST:event_btnApagarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(this.txtNomeDoProduto.getText().isEmpty() || this.txtPrecoDoProduto.getText().isEmpty() || this.txtQuantidadeDeProdutos.getText().isEmpty())
             JOptionPane.showMessageDialog(null,"Todos os campos devem ser preenchidos!");
        else{
        if(this.tabelaDeProdutos.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(null,"Você deve selecionar um Produto");
        else{
           Produto produto = new Produto();
           produto.setCodigoDoProduto(produtos.get(this.tabelaDeProdutos.getSelectedRow()).getCodigoDoProduto());
           produto.setNomeDoProduto(this.txtNomeDoProduto.getText().trim());
           produto.setPrecoDoProduto(Double.parseDouble(this.txtPrecoDoProduto.getText().trim()));
           produto.setQuantidadeDeProdutos(Integer.parseInt(this.txtQuantidadeDeProdutos.getText().trim()));
           produto.setCodigoDeBarraDoProduto(this.txtCodigoDeBarraDoProduto.getText().trim());
           dao.atualiza(produto);
           lerDadosIniciais();
          }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed



