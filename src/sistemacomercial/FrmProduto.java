    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Produto produto = new Produto();
        produto.setNomeDoProduto(this.txtNomeDoProduto.getText().trim());
        produto.setPrecoDoProduto(Double.parseDouble(this.txtPrecoDoProduto.getText().trim().replace(',','.')));
        produto.setQuantidadeDeProdutos(Integer.parseInt(this.txtQuantidadeDeProdutos.getText().trim()));
        produto.setCodigoDeBarraDoProduto(this.txtCodigoDeBarraDoProduto.getText().trim());
        dao.insere(produto);
        JOptionPane.showMessageDialog(null,"Produto Inserido com Sucesso!");
        this.lerDadosIniciais();
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

